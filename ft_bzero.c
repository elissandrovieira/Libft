#include "libft.h"

void	bzero(void *s, size_t n)
{
	char	*s1;
	char	*end;

	s1 = (char *)s;
	end = s1 + n;
	while (s1 != end)
	{
		*s1 = '\0';
		s1++;
	}
	return (s);
}
